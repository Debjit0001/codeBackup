import java.util.Scanner;

public class Loops {
    public static void main(String[] args) {
        
        for (int i = 1; i <= 5; i++) {
            System.out.println(i);
        }

        int i = 1;
        while(i <= 10) {
            System.out.println("Hello World!");
        }

        do {
            System.out.println(i);
        } while (i != 1);
    }
}
